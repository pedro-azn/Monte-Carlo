def multiplier(a, b):
    return a * b  # Renvoie le produit de a et b

# Appel de la fonction et stockage du résultat dans une variable
produit = multiplier(3, 4)

# Réutilisation de la variable 'produit'
print("Le produit est :", produit)  # Affiche: Le produit est : 12

# Vous pouvez également effectuer d'autres opérations avec 'produit'
double_produit = produit * 2
print("Le double du produit est :", double_produit)  # Affiche: Le double du produit est : 24